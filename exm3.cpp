#include <stdio.h>

int main() {
    // Значения вершин
    int heap[9] = { 0, 1, 3, 4, 6, 8, 11, 13, 16 };

    // Массив с индексами потомков: для каждого узла — {левый, правый}
    // Если потомок отсутствует, -1
    int children[9][2] = {
        { -1, -1 },   // 0 - не исп.
        { 2, 3 },     // 1
        { 4, 5 },     // 2
        { 6, 7 },     // 3
        { 8, -1 },    // 4
        { -1, -1 },   // 5
        { -1, -1 },   // 6
        { -1, -1 },   // 7
        { -1, -1 }    // 8
    };

    // Вывод
    for (int i = 1; i <= 8; i++) {
        printf("Node %d (value=%d): left child index = %d, right child index = %d\n",
            i, heap[i], children[i][0], children[i][1]);
    }

    return 0;
}
